// Declare variables
var x = 10;
let y = 20;
const z = 30;

// Display variable values
console.log("Value of x: " + x);
console.log("Value of y: " + y);
console.log("Value of z: " + z);

// Modify variable values
x = 15;
y = 25;
// z = 35; // Uncommenting this line will result in an error because z is a constant

// Display modified variable values
console.log("Modified value of x: " + x);
console.log("Modified value of y: " + y);

// Variables with different data types
let name = "John";
let age = 25;
let isMarried = false;

// Display information in a sentence
console.log(
  "Name: " + name + ", Age: " + age + " years, Marital Status: " + isMarried
);

// Array variable
let numbers = [1, 2, 3, 4, 5];

// Display array contents
console.log("Contents of the numbers array: " + numbers);

// Object variable
let person = {
  name: "Jane",
  age: 30,
  occupation: "Programmer",
};

// Display object properties
console.log(
  "Name: " +
    person.name +
    ", Age: " +
    person.age +
    ", Occupation: " +
    person.occupation
);
